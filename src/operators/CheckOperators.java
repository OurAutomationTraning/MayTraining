package operators;

public class CheckOperators {
    public static void main(String[] args) {
        int a=10;
        int b=12;
        if(a==b) {
            System.out.println("variables are same");
        } else if(a>=b) {
            System.out.println("a is greater than b");
        } else if(a<=b) {
            System.out.println("a is less than b");
        }
    }
}
